    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validn()==true){
            try {
                Connection myconn = DriverManager.getConnection(url,dbuser,dbpass);
                try{
                    String q = "insert into employee values(?,?,?,?,?,?,?,?,?,?)";
                    PreparedStatement myst = myconn.prepareStatement(q);
                    myst.setString(1, jTextField1.getText());
                    if(jRadioButton1.isSelected()==true)
                    {
                        myst.setString(2, "Male");
                    }
                    else if(jRadioButton2.isSelected()==true)
                    {
                        myst.setString(2, "Female");
                    }
                    myst.setString(3, jTextArea1.getText());
                    SimpleDateFormat myformat = new SimpleDateFormat("yyyy-MM-dd");
                    String dob = myformat.format(jDateChooser1.getDate());
                    myst.setString(4, dob);
                    myst.setString(5, jComboBox1.getSelectedItem().toString());
                    myst.setString(6, jTextField2.getText());
                    myst.setString(7, jTextField3.getText());
                    myst.setString(8, jPasswordField1.getText());
                    myst.setString(9, "2018-1-1");
                    myst.setString(10, "admin");
                    int count = myst.executeUpdate();
                    if (count == 1)
                    {
                        JOptionPane.showMessageDialog(rootPane, "Admin created successfully");
                        this.dispose();
                        Login obj = new Login();
                        obj.setVisible(true);
                    }
                    else if(count == 0)
                    {
                        JOptionPane.showMessageDialog(rootPane , "Admin not created ");
                    }
                } catch(Exception e)
                {
                    JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
                }
                finally
                {
                    myconn.close();
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error in connection " + e.getMessage());

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

