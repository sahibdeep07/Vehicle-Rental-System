     boolean validn()
             {
             if(jTextField1.getText().length()<3)
             {
             JOptionPane.showMessageDialog(rootPane, "Enter proper name");
             return(false);
             }
             else if(jTextArea1.getText().length()<3)
             {
             JOptionPane.showMessageDialog(rootPane, "Enter proper address");
             return(false);
             }
             else if(jTextField2.getText().length()!=10)
             {
             JOptionPane.showMessageDialog(rootPane, "Enter proper phone number");
             return(false);
             }
             else if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false)
             {
             JOptionPane.showMessageDialog(rootPane, "Choose Gender");
             return(false);
             }
             else if(jComboBox1.getSelectedIndex()==0)
             {
             JOptionPane.showMessageDialog(rootPane, "Select Qualification");
             return(false);
             }
             else if(jDateChooser1.getDate()==null)
             {
             JOptionPane.showMessageDialog(rootPane, "Please enter Date of Birth");
             return(false);
             }
             else
             {
             return(true);
             }
             }

